/**
 * 底部导航栏组件
 * 特点：固定在底部，支持徽章显示，激活状态视觉反馈
 */
export function BottomNavigation() {
  const pathname = usePathname()

  return (
    <nav className="fixed bottom-0 left-0 right-0 bg-white border-t border-gray-200 z-50">
      <div className="flex items-center justify-around h-16 px-2">
        {navigationItems.map((item) => {
          const isActive = pathname === item.href || 
            (item.href !== '/' && pathname.startsWith(item.href))
          
          return (
            <Link
              key={item.id}
              href={item.href}
              prefetch={true}  // 启用预加载优化
              className={cn(
                'flex flex-col items-center justify-center min-w-[44px] min-h-[44px] px-2 py-1 rounded-lg transition-colors',
                'hover:bg-gray-100 active:bg-gray-200',
                isActive 
                  ? 'text-blue-600' 
                  : 'text-gray-600 hover:text-gray-900'
              )}
            >
              <div className="relative">
                <item.icon className="w-6 h-6" />
                {/* 徽章显示 */}
                {item.badge && item.badge > 0 && (
                  <span className="absolute -top-1 -right-1 bg-red-500 text-white text-xs rounded-full min-w-[16px] h-4 flex items-center justify-center px-1">
                    {item.badge > 99 ? '99+' : item.badge}
                  </span>
                )}
              </div>
              <span className={cn(
                'text-xs mt-1 font-medium',
                isActive ? 'text-blue-600' : 'text-gray-600'
              )}>
                {item.label}
              </span>
            </Link>
          )
        })}
      </div>
      
      {/* 安全区域适配 */}
      <div className="h-[env(safe-area-inset-bottom)] bg-white"></div>
    </nav>
  )
}